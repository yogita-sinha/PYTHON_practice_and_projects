# append used to add an element at the end 

arr = [5,3,7,2,8]
arr.append(6)
print(arr)

# sorting list in ascending order
arr.sort()
print(arr)

# sorting list in descending order
arr.sort(reverse=True)
print(arr)

#arr reverse
arr.reverse()
print(arr)

#to insert particular element at pertivular index

arr.insert(0,1)
print(arr)

arr.insert(3,4)
print(arr)

arr.insert(3,4)
print(arr)  

#to remove element from list
arr.remove(4)
print(arr)

#to remove element from list at particular index
arr.pop(7)
print(arr)
